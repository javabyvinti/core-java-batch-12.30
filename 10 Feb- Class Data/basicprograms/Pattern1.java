package com.java.basicprograms;

/*
 *****	row = 5 column = 5
 *****
 *****
 *****
 *****
 */


public class Pattern1 {

	public static void main(String[] args) {
		
		for(int row = 1; row<=5; row++){
			for(int column =1; column<=5; column++){
				System.out.print("*");
			}
		System.out.println();	
		}
		

	}

}
